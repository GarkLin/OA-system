package ssh.entity;

import java.util.Date;

public class Finance {
	private int fid;
	private String finance_type;
	private int finance_id;
	private String finance_name;
	private String finance_department;
	private String finance_money;
	private String finance_date;
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFinance_type() {
		return finance_type;
	}
	public void setFinance_type(String finance_type) {
		this.finance_type = finance_type;
	}
	public int getFinance_id() {
		return finance_id;
	}
	public void setFinance_id(int finance_id) {
		this.finance_id = finance_id;
	}
	public String getFinance_name() {
		return finance_name;
	}
	public void setFinance_name(String finance_name) {
		this.finance_name = finance_name;
	}
	public String getFinance_department() {
		return finance_department;
	}
	public void setFinance_department(String finance_department) {
		this.finance_department = finance_department;
	}
	public String getFinance_money() {
		return finance_money;
	}
	public void setFinance_money(String finance_money) {
		this.finance_money = finance_money;
	}
	public String getFinance_date() {
		return finance_date;
	}
	public void setFinance_date(String finance_date) {
		this.finance_date = finance_date;
	}

}
